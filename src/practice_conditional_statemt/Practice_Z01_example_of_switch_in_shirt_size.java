package practice_conditional_statemt;

public class Practice_Z01_example_of_switch_in_shirt_size {
	public static void main(String[] args) {
		char size ='P';
		
		switch (size) {
		case 'L':
			System.out.println("Yes we have your Large size");
			break;
 
			
		case 'M':
			System.out.println("Yes we have your Medium size");
			break;
			
		case 'S':
			System.out.println("Yes we have your Small size");
			break;
			
			
			
		default:
			System.out.println("Sorry we don't have your Size ");
			break;
		}
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
