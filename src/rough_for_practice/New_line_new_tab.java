package rough_for_practice;

public class New_line_new_tab {
	public static void main(String[] args) {
		System.out.println("My Name is Ifzal and I love eating and cooking. I like travelling too ");
		// You must write \n\t , if you write \t\n = does not give tab . just makes new line
		// Below both guve same outcome
		System.out.println("My Name is Ifzal \n\tand I love eating and cooking. \n\tI like travelling too");
		System.out.println("My Name is Ifzal" +  "\n\tand I love eating and cooking." +  "\n\tI like travelling too");
	 
		
		
	}

}
